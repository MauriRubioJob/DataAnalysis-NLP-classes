# Write a Python program to calculate the total of all the items in a
# list using a for loop.

# Posic.:	0 , ...                          , 8
numbers = [91, 156, 98, 79, 62, 44, 164, 73, 7]

# Debo sumar todos y cada uno de los elementos de mi lista.

# Crear una varibale que me va a almacenar la suma de cada elemento.
total = 0

# Vamos a crear un bucle for que me recorrar la lista.
for n in numbers:
    total = total + n

# Imprimir con print en pantalla Ãºnicamente el resultado del ejercicio.
print(total)
